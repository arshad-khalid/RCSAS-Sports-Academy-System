    private void submitbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbutActionPerformed
        try
        {
            File f = new File("sportRecords.txt");
            String feedback = pfeedbacktxt.getText().trim();
            String star = startxt.getText().trim();
            String sportid= sportID.getText().trim();
            Scanner x=new Scanner(f);
                    
            try(PrintWriter writer = new PrintWriter(new BufferedWriter(new FileWriter("Feedback_and_Star.txt",true))))
            {
                while (x.hasNext()) {                    
                    String line=x.nextLine();
                    if (line.contains(sportid)) {
                        
                        sportid=line;
                        String sport=x.nextLine();
                        String location= x.nextLine();
                        String coachid = x.nextLine();
                        
                        writer.println(coachid);
                        writer.println(sport);
                        writer.println(location);
                        writer.println(feedback);
                        writer.println(star);
                    }
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error","Error message", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error","Error message", JOptionPane.ERROR_MESSAGE);
        }
        this.setVisible(false);
        tqsubmit tq = new tqsubmit();
        tq.setVisible(true);
    }//GEN-LAST:event_submitbutActionPerformed

