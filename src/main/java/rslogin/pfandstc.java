    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        try {
            File f = new File("sportRecords.txt");
            Scanner x=new Scanner(f);
            String sportid=sportID.getText().trim();
            
            while (x.hasNext()) {                    
                String line=x.nextLine();
                if (line.contains(sportid)) {
                    sportid=line;
                    String sport=x.nextLine();
                    String location= x.nextLine();
                    String coachid = x.nextLine();
                    
                    coachIDTextfield.setText(coachid);
                    SPORTtextfield.setText(sport);
                    locationTextfield.setText(location);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

